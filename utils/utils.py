def get_basket(article: int) -> str:
    short_id = int(article) // 100000
    ranges = [
        (0, 143, "01"),
        (144, 287, "02"),
        (288, 431, "03"),
        (432, 719, "04"),
        (720, 1007, "05"),
        (1008, 1061, "06"),
        (1062, 1115, "07"),
        (1116, 1169, "08"),
        (1170, 1313, "09"),
        (1314, 1601, "10"),
        (1602, 1655, "11"),
        (1656, 1919, "12"),
        (1920, 2045, "13"),
        (2046, 2189, "14"),
        (2190, 2405, "15"),
    ]

    for start, end, basket in ranges:
        if start <= short_id <= end:
            return basket
    return "16"
